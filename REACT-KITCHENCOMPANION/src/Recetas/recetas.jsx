// Lasaña

//Ingredientes:

// 9 láminas de pasta para lasaña
// 500 g de carne molida (puede ser de res o cerdo)
// 1 cebolla picada
// 2 dientes de ajo picados
// 1 zanahoria rallada
// 1 tomate triturado (400 g)
// Salsa de tomate
// 1 cucharadita de orégano seco
// ½ cucharadita de albahaca seca
// Sal y pimienta al gusto
// 2 tazas de queso mozzarella rallado
// 1 taza de queso parmesano rallado (opcional)
// Aceite de oliva para cocinar
// Instrucciones:

// Precalienta el horno a 180°C.

// En una sartén grande, calienta un poco de aceite de oliva a fuego medio-alto. Agrega la cebolla y el ajo, y cocina hasta que estén dorados y fragantes.

// Añade la carne molida a la sartén y cocínala hasta que esté bien dorada y cocida por completo. Rompe los grumos de carne con una cuchara de madera.

// Agrega la zanahoria rallada y continúa cocinando por unos minutos hasta que esté tierna.

// Vierte el tomate triturado y la salsa de tomate en la sartén. Condimenta con orégano, albahaca, sal y pimienta al gusto. Mezcla bien todos los ingredientes y deja que la salsa de carne se cocine a fuego medio-bajo durante unos 10-15 minutos, para que los sabores se mezclen.

// Mientras tanto, en una olla aparte, cuece las láminas de pasta para lasaña según las instrucciones del paquete. Escúrrelas y enjuágalas con agua fría para detener la cocción.

// En un molde apto para horno, coloca una capa de láminas de pasta en el fondo. Luego, agrega una capa de la salsa de carne sobre la pasta, esparciéndola de manera uniforme. Espolvorea queso mozzarella y queso parmesano rallados sobre la salsa.

// Repite el proceso de capas: láminas de pasta, salsa de carne y quesos rallados, hasta que hayas utilizado todos los ingredientes. Termina con una capa de láminas de pasta y queso rallado por encima.

// Cubre el molde con papel de aluminio y hornea durante 25 minutos. Luego, retira el papel de aluminio y hornea por otros 10-15 minutos, o hasta que el queso esté dorado y burbujeante.

// Retira del horno y deja reposar durante unos minutos antes de cortar y servir la lasaña.

// ¡Disfruta de esta deliciosa lasaña casera junto con una ensalada fresca!



