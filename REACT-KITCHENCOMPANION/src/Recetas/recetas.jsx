// RISOTTO DE CHAMPIÑONES

// Ingredientes:

// 1 taza de arroz arborio (o cualquier otro arroz para risotto)
// 200 g de champiñones, lavados y cortados en rodajas
// 1 cebolla pequeña, picada finamente
// 2 dientes de ajo, picados
// 4 tazas de caldo de verduras o pollo
// 1/2 taza de vino blanco (opcional)
// 2 cucharadas de mantequilla
// 1/4 taza de queso parmesano rallado
// Sal y pimienta al gusto
// Perejil fresco picado para decorar (opcional)
// Instrucciones:

// En una olla grande, calienta una cucharada de mantequilla a fuego medio. Agrega la cebolla picada y el ajo, y cocínalos hasta que estén transparentes y fragantes.

// Añade los champiñones a la olla y cocina hasta que estén tiernos y dorados.

// Agrega el arroz arborio a la olla y revuélvelo para cubrirlo con la mantequilla y los jugos de los champiñones. Cocina durante un minuto para que los granos de arroz se impregnen de los sabores.

// Si deseas, puedes agregar el vino blanco y cocinar hasta que se evapore.

// Comienza a agregar el caldo de verduras o pollo caliente, una taza a la vez, revolviendo constantemente y esperando a que el arroz absorba el líquido antes de agregar más. Continúa este proceso hasta que el arroz esté al dente y haya absorbido la mayor parte del caldo. Esto puede tomar alrededor de 20 minutos.

// Retira la olla del fuego y agrega la cucharada restante de mantequilla y el queso parmesano rallado. Revuelve hasta que la mantequilla se derrita y el queso se incorpore, formando una textura cremosa.

// Prueba el risotto y sazónalo con sal y pimienta al gusto.

// Sirve el risotto de champiñón en platos individuales y decora con perejil fresco picado si lo deseas.

// ¡Disfruta de este delicioso risotto de champiñón! Es un plato reconfortante y sabroso que puedes disfrutar como plato principal o como guarnición.




