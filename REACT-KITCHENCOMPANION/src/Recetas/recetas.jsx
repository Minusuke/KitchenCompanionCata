// pastel de choclo:

// Ingredientes:

// 4 tazas de granos de choclo (maíz) frescos o congelados
// 1 taza de leche
// 2 cucharadas de mantequilla
// 1 cucharada de azúcar
// Sal al gusto
// 500 g de carne molida (puede ser de res o pollo)
// 1 cebolla picada
// 2 dientes de ajo picados
// 1 pimiento rojo picado
// 1 zanahoria rallada
// ½ taza de aceitunas sin hueso (opcional)
// ½ taza de pasas (opcional)
// Sal y pimienta al gusto
// Queso rallado para espolvorear (opcional)
// Instrucciones:

// Precalienta el horno a 180°C.

// En una licuadora o procesadora de alimentos, mezcla los granos de choclo, la leche, la mantequilla, el azúcar y la sal hasta obtener una mezcla suave. Reserva.

// En una sartén grande, cocina la carne molida a fuego medio-alto hasta que esté dorada y cocida por completo. Agrega la cebolla, el ajo, el pimiento y la zanahoria, y cocina hasta que las verduras estén tiernas.

// Si deseas, añade las aceitunas y las pasas a la mezcla de carne y verduras. Condimenta con sal y pimienta al gusto. Mezcla bien.

// En un molde apto para horno, vierte la mitad de la mezcla de choclo y distribúyela de manera uniforme en el fondo.

// Agrega la mezcla de carne y verduras sobre la capa de choclo en el molde. Nivela la superficie.

// Cubre el relleno de carne con la otra mitad de la mezcla de choclo, asegurándote de cubrir completamente el relleno.

// Si lo deseas, espolvorea queso rallado por encima del pastel.

// Hornea durante aproximadamente 45-50 minutos, o hasta que la superficie esté dorada y el pastel esté firme al tacto.

// Retira del horno y deja enfriar un poco antes de cortar en porciones y servir.

// ¡Disfruta de este delicioso pastel de choclo como plato principal! Puedes acompañarlo con ensalada o disfrutarlo tal cual.
