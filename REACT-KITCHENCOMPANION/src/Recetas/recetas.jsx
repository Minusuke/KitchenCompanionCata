//   ceviche


// Ingredientes:

// 400 g de filete de pescado blanco fresco (como el lenguado o la corvina), cortado en trozos pequeños
// 1/2 cebolla roja, cortada en rodajas finas
// 1 tomate grande, sin semillas y cortado en cubitos
// 1 chile jalapeño o ají picante, sin semillas y picado finamente
// El jugo de 3 limones
// Un puñado de cilantro fresco, picado
// Sal y pimienta al gusto
// Tostadas o chips de maíz para servir
// Instrucciones:

// En un tazón, coloca el pescado cortado en trozos y rocíalo con el jugo de limón. Asegúrate de que todos los trozos de pescado estén cubiertos con el jugo. Deja marinar en el refrigerador durante aproximadamente 15-20 minutos.

// Agrega la cebolla roja, el tomate y el chile jalapeño al tazón con el pescado marinado. Mezcla suavemente todos los ingredientes.

// Añade el cilantro picado y sazona con sal y pimienta al gusto. Mezcla nuevamente para combinar todos los sabores.

// Deja reposar el ceviche en el refrigerador durante al menos 10 minutos más para que los sabores se mezclen y el pescado se marine completamente.

// Sirve el ceviche de pescado en tostadas o con chips de maíz. Puedes decorar con rodajas de limón y hojas de cilantro adicionales, si lo deseas.

// ¡Disfruta de este refrescante y delicioso ceviche de pescado! Es perfecto como entrada o plato principal en días calurosos.
