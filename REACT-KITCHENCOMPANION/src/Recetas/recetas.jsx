// // Arepas venezolanas con relleno Reina Pepiada


// Ingredientes:

// 2 tazas de harina de maíz precocida (preferiblemente para arepas)
// 1 y ½ tazas de agua tibia
// ½ cucharadita de sal
// 1 pechuga de pollo cocida y desmenuzada
// 1 Palta
// ½ cebolla picada
// 2 cucharadas de mayonesa (opcional)
// Jugo de medio limón
// Sal y pimienta al gusto
// Aceite vegetal para cocinar
// Instrucciones:

// En un tazón, mezcla la harina de maíz precocida con la sal. Agrega el agua tibia gradualmente y mezcla con las manos hasta obtener una masa suave y maleable. Deja reposar por unos minutos.

// Divide la masa en porciones y forma bolas. Aplasta cada bola con las manos para formar las arepas, de aproximadamente ½ pulgada de espesor.

// Calienta una sartén o plancha a fuego medio-alto. Cocina las arepas en la sartén caliente durante unos 5 minutos por cada lado, o hasta que estén doradas y crujientes por fuera.

// Mientras tanto, prepara el relleno. En un tazón, mezcla la pechuga de pollo desmenuzada, el aguacate machacado, la cebolla picada, la mayonesa y el jugo de limón. Condimenta con sal y pimienta al gusto y mezcla bien todos los ingredientes.

// Corta las arepas por la mitad y rellénalas generosamente con la mezcla de reina pepiada.

// ¡Sirve las arepas venezolanas rellenas de reina pepiada y disfrútalas calientes!

// Puedes acompañarlas con ensalada o guarniciones de tu elección. ¡Buen provecho!
