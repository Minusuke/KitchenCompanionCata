// PAELLA DE MARISCOS


// INGREDIENTES


// Para el caldo de pescado:

//  1 kg de espinas y/o cabeza de pescado
// 1 cebolla cortada en cuartos
// ½ zanahoria cortada en cuartos
// 1 ramita de hojas de apio
// ½ limón
// ½ tomate cortado en cuartos
// 3 dientes de ajo machacados
// 8 granos de Pimienta Negra Entera Gourmet
// Aceite de oliva
// 3  lt de agua fría
// Para el sofrito:

//  ½ cebolla
// ½ pimentón verde
// ½ pimentón rojo
// 4 dientes de ajo
// 100ml de aceite de oliva
 

// Para el resto de la paella:

//  6 calamares
// 200gr de camarones chicos
// 350gr de arroz grano corto
// 1 cdta de Pimentón Paprika Gourmet
// 1 cdta de Azafrán en Hebras Gourmet
// 2 tomates picados
// 4 camarones 36/40 crudos y enteros
// 16 choritos limpios con concha
// 12 almejas limpias con concha


// PREPARACIÓN


// 1 Limpiar y cortar el pescado. Colocar los trozos en una fuente y refrigerar.
// 2 Preparar el sofrito: Cortar la cebolla y pimentones en cuadraditos chicos y poner con los ajos y aceite de oliva en un bowl. Reservar.
// 3 Preparar el fondo de pescado: dorar las verduras para el fondo de pescado en una olla con un poco de aceite junto al resto de los ingredientes para el fondo, cuidando que no se quemen. Agregar el agua fría y las espinas o cabeza de pescado. Cuando hierva bajar el fuego y cocinar sin que hierva durante 35 minutos. Colar y desechar los sólidos, reservar el caldo caliente.
// 4 Preparar la paella: poner el sofrito en la paella, salar y cocinar a fuego medio. Limpiar y cortar los calamares en aros y agregarlos junto a los camarones chicos a la paella, sofreír unos minutos y agregar el arroz.  Sofreír unos minutos más.
// 5 Agregar el Pimentón Paprika Gourmet y el Azafrán en Hebras , revolver bien, agregar el tomate y sofreír unos minutos más.
// 6 Agregar 650ml de caldo de pescado caliente, sazonar con sal a gusto y dejar unos minutos a fuego alto. 3-4 minutos después agregar una taza más de caldo y bajar el fuego.
// 7 La cocción de la paella no debe tomar más de 18-20 minutos desde que se agrega el caldo. Basándose en esto, cuando falten 5 minutos, agregar el resto de los mariscos y caldo hasta completar 3 veces de caldo por 1 de arroz (1,5 lts de caldo en total aprox).


